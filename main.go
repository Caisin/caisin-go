package main

import (
	"log"

	"github.com/fsnotify/fsnotify"
)

func main() {
	watcher, err := fsnotify.NewWatcher()
	if err != nil {
		log.Fatal(err)
	}
	defer watcher.Close()

	done := make(chan bool)
	go func() {
		for {
			select {
			case event, ok := <-watcher.Events:
				if !ok {
					return
				}
				switch event.Op {
				case fsnotify.Write:
				case fsnotify.Remove:
				case fsnotify.Create:
				case fsnotify.Rename:
				case fsnotify.Chmod:

				}
			case err, ok := <-watcher.Errors:
				if !ok {
					return
				}
				log.Println("error:", err)
			}
		}
	}()

	err = watcher.Add("E:/code/go/caisin-go/test.txt")
	if err != nil {
		log.Fatal(err)
	}
	<-done
}
